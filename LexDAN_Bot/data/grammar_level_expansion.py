def _ex_bank(
    *,
    mcq: list[tuple],
    words: list[tuple],
    tr_en: tuple,
    tr_ru: tuple,
) -> list[dict]:
    """mcq: (instr, en, ru, options, answer, tip); words similar; tr: (instr, src, answer, tip, accept?)."""
    out: list[dict] = []
    for instr, en, ru, opts, ans, tip in mcq:
        out.append(
            {
                "kind": "mcq",
                "subtype": "mcq",
                "instruction_ru": instr,
                "sentence_en": en,
                "sentence_ru": ru,
                "options": opts,
                "answer": ans,
                "tip": tip,
            }
        )
    for instr, en, ru, base, ans, tip in words:
        item = {
            "kind": "write",
            "subtype": "word_form",
            "instruction_ru": instr,
            "sentence_en": en,
            "sentence_ru": ru,
            "base_form": base,
            "answer": ans,
            "tip": tip,
        }
        out.append(item)
    instr, src, ans, tip = tr_en[:4]
    item = {
        "kind": "write",
        "subtype": "translate_en",
        "instruction_ru": instr,
        "sentence_ru": src,
        "sentence_en": "",
        "answer": ans,
        "tip": tip,
    }
    if len(tr_en) > 4:
        item["accept"] = list(tr_en[4])
    out.append(item)
    instr, src, ans, tip = tr_ru[:4]
    item = {
        "kind": "write",
        "subtype": "translate_ru",
        "instruction_ru": instr,
        "sentence_en": src,
        "sentence_ru": ans,
        "answer": ans,
        "tip": tip,
    }
    if len(tr_ru) > 4:
        item["accept"] = list(tr_ru[4])
    out.append(item)
    return out


