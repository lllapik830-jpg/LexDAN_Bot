def has_vocabulary_level(level: str) -> bool:
    _ensure_high_words()
    return level in WORDS and bool(WORDS[level])


def get_words(level: str, topic_id: str) -> list[dict]:
    _ensure_high_words()
    return list(WORDS.get(level, {}).get(topic_id, []))


def get_word_entry(level: str, topic_id: str, en: str) -> dict | None:
    key = (en or "").strip().lower()
    for w in get_words(level, topic_id):
        if w["en"].lower() == key:
            return w
    return None


def words_total(level: str, topic_id: str) -> int:
    return len(get_words(level, topic_id))


_HIGH_WORDS_LOADED = False


def _ensure_high_words() -> None:
    global _HIGH_WORDS_LOADED
    if _HIGH_WORDS_LOADED:
        return
    from data.vocab_high import HIGH_WORDS

    WORDS.update(HIGH_WORDS)
    _HIGH_WORDS_LOADED = True
